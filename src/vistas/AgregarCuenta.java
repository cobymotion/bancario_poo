    private void btnCrearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearCuentaActionPerformed
        // TODO add your handling code here:
        double valorInicialCuenta = 0;
          try{
                valorInicialCuenta = Double.parseDouble
                                       (txtInicialCuenta.getText());                
            }catch(NumberFormatException e){
                lblError.setVisible(true);
                return;
            }
        if(valorInicialCuenta<0){
            lblError.setVisible(true);
            return; 
        }  
        String titular = txtTitular.getText().trim(); 
        if(titular.isEmpty())
        {
            lblError.setVisible(true);
            return; 
        }
        Cuenta cuenta = null; 
        switch(tipoCuenta){
            case 1: 
                int edad = Integer.parseInt(jtEdad.getText()); 
                System.out.println("Edad: " + edad);
                if(edad <1 || edad>18){
                    lblError.setVisible(true);
                    return;
                }
                else {
                    cuenta = new CuentaJunior(titular, valorInicialCuenta, edad);                
                }
                break; 
            case 2:
                cuenta = new CuentaPremium(titular, valorInicialCuenta);
                break; 
            default: 
                lblError.setVisible(true);
                return; 
        }
        this.dispose();                
        DetalleCuenta dc = new DetalleCuenta
                             (null,true,cuenta);
        dc.setVisible(true);
    }//GEN-LAST:event_btnCrearCuentaActionPerformed

